package main.java.com.mapdata;

public class Metadata {
    private String id;
    private String type;
    private double rating;
    private int reviews;

    // Constructor, Getters, and Setters
    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public double getRating() {
        return rating;
    }

    public int getReviews() {
        return reviews;
    }
}
